
int main()
{
 
  int i;
  int k;
  int sum;
  int b[10];
  k = cin;
 
  sum = 0;

  for(i=0; i < 10; i = i+1){

     b[0] = 10;  
     cout = b[0];
  }

  for(i=0; i < k; i = i+1){

     b[i] = k+b[i];  
     cout = b[i];
  }
  for(i=0; i<k; i = i+1){
    sum = sum + b[i];
    cout = sum;
  }

  cout = sum;
  return 0;
}
